def grid(self):
    for i in range(50):
        for j in range(50):
            pygame.draw.line(sc, WHITE, (0, H * i * tile_size), (0 + 10 * tile_size, H * i * tile_size), 1)


class World:
    def __init__(self, data):
        self.tile_list = []
        grid()

        dirt = pygame.image.load('Assets/Images/earthblock.png')
        spike = pygame.image.load('Assets/Images/spike.png')

        row_count = 0

        for row in data:
            col_count = 0
            for tile in row:
                if tile == 1:
                    img = pygame.transform.scale(dirt, (tile_size, tile_size))
                    img_rect = img.get_rect()
                    img_rect.x = col_count * tile_size
                    img_rect.y = col_count * tile_size
                    tile = (img, img_rect)
                    self.tile_list.append(tile)

                if tile == 2:
                    img_spike = pygame.transform.scale(spike, (tile_size, tile_size))
                    img_spike_rect = img_spike.get_rect()
                    img_spike_rect.x = col_count * tile_size
                    img_spike_rect.y = col_count * tile_size
                    tile = (img_spike, img_spike_rect)
                    self.tile_list.append(tile)


                col_count += 1
            row_count += 1



    def draw(self):
        for tile in self.tile_list:
            sc.blit(tile[0], tile[1])
            pygame.draw.rect(sc, (255, 255, 255), tile[1], 2)
